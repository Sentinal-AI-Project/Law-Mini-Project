                    padding: '0.25rem 0.75rem', 
                    borderRadius: '20px', 
                    fontSize: '0.8rem', 
                    fontWeight: 600,
                    background: doc.risk === 'Critical' ? '#fee2e2' : doc.risk === 'Medium' ? '#fef3c7' : '#f1f5f9',
